    <h1>What makes a Big Climate Impact candidate?</h1>
    <p>Whether a candidate is Democratic, Republican, or a third party, we'd be
        happy to recommend them if they are proposing realistic paths to
        addressing climate change. Even better if they're an incumbent with a
        proven track record!
    </p>
    <p>Of course, it's not <em>quite</em> that simple, because we want to use
        our votes strategically to make a big impact.
    </p>
    <h2>1. How they approach climate change</h2>
    <p>There are at least three main categories of candidates on the issue
        of climate change:
    </p>
    <ol>
        <li>Supports a wide-variety of steps to reach net zero emissions
            rapidly.
        </li>
        <li>Questions the "science" of climate change, instead talking about
            "natural cycles".
        </li>
        <li>Has a platform of "clean coal" and carbon capture.</li>
    </ol>
    <p>A majority of Americans believe that <a href="https://www.nbcnews.com/politics/meet-the-press/global-warming-perceptions-states-more-americans-accept-fault-n1265213">global warming is caused by
        human activity</a> and <a href="https://www.theverge.com/2021/5/26/22454633/pew-research-american-attitudes-climate-change-fossil-fuels-renewable-energy">close to two-thirds see it as an urgent problem to address</a>.
        That means we don't really need to worry about those people who are still
        claiming that it's not happening.
    </p>
    <p>We really <em>do</em> need to worry about those who claim to support
        efforts to combat climate change but offer up non-solutions like
        <a href="https://www.factcheck.org/2018/11/clearing-up-the-facts-behind-trumps-clean-coal-catchphrase/">"clean coal"</a>
        or technologies that are nowhere near capable of solving the problem
        like carbon capture. These people are a grave threat to addressing
        climate change, because they'll draw attention away from real solutions.
    </p>
    <p>So, as a first cut, the candidate needs to support real solutions. That
        said, we may still support candidates who fall into the next categories,
        but we'll make the reason for our support clear.
    </p>
    <h2>2. Electability and ability to boost other BCI candidates</h2>
    <p>Without a doubt, there will be third party candidates with worthy
        plans for climate policy, but unless their election is determined
        through <a href="https://www.fairvote.org/rcv#where_is_ranked_choice_voting_used">ranked choice voting</a>,
        they will almost certainly lose.
    </p>
    <p>There may be a candidate who doesn't have a good plan for climate but
        whose election will provide more power to others who do through
        things like committee assignments. Barring a better candidate showing
        up, we could still support them.
    </p>
    <h2>3. We will not support an anti-democracy candidate</h2>
    <p>A disturbing number of candidates support baseless claims of widespread
        voter fraud and seek to make voting more difficult for millions of
        legitimate voters (a majority of whom they believe wouldn't vote for
        them). Such candidates would be bad for America, and almost certainly
        fall in behind the financial interests who stand opposed to moving
        forward with clean energy. These people will not find support on this
        site.
    </p>

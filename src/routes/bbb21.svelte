    <p>Update for December 20, 2021: You may have seen the news that Senator
        Manchin yesterday said that he won't support Build Back Better. Today,
        Senate Majority Leader Schumer said that <a href="https://www.reuters.com/world/us/schumer-says-senate-vote-biden-plan-despite-manchin-objections-2021-12-20/">he's going to call for a
        vote on BBB regardless</a>. So, keep up the pressure on the Senate
        to try to get this to pass. If it doesn't, we'll need to pivot to
        get the House to pass a new bill which can make it through the Senate.
    </p>
    <p>On November 19, 2021, the House of Representatives <a href="https://www.nrdc.org/experts/manish-bapna/house-passes-historic-climate-action-build-back-better-act">passed the
        Build Back Better Act</a>, which is the most significant climate
        legislation passed to date. Now, it's in the Senate's hands, so
        you should <a href="https://www.senate.gov/senators/senators-contact.htm">contact
        your Senator</a> and let them know you support the Build Back Better Act,
        especially its climate provisions.
    </p>
    <p>You've undoubtedly seen very large numbers thrown around:
        "A $1.75 trillion bill" or "$555 billion for climate action". It's
        important to note that these figures are for <em>10 years</em>.
        We spend <a href="https://www.pgpf.org/budget-basics/budget-explainer-national-defense">more than $700 billion every year on the military</a>.
        That means our military budget is 10 times what we're talking about
        spending in this bill to address climate change.
    </p>
    <h2>What Build Back Better gets us, in brief</h2>
    <p>This <a href="https://www.nrdc.org/experts/manish-bapna/house-passes-historic-climate-action-build-back-better-act">NDRC article</a> and
    the <a href="https://www.whitehouse.gov/build-back-better/">White House page</a> about the bill
    both provide good detail. Here's just a quick bullet list of what the bill provides for climate action:
    </p>
    <ul>
        <li>Rebates and tax credits to help middle class families shift to clean electricity and vehicles,</li>
        <li>Incentives to make sure US companies are manufacturing what we need in decarbonizing our economy,</li>
        <li>Help for the deployment of cleaner transit and more energy efficient homes, including within low-income communities, and</li>
        <li>Investment in smarter agriculture and improved stewardship of forests.</li>
    </ul>

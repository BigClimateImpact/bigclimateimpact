<h2>Preliminary recommendations for Wyoming's primary elections</h2>
<p>Wyoming is a tough one for climate change, because fossil fuel industries
    are large and powerful in the state. Ideally, the state would be pushing to
    encourage new forms of energy investment, but here we are.
</p>
<p>Wyoming is a heavily Republican state, with about 70% of the presidential
    vote going to the Republican candidate in each of the last three elections.
    The balance of the state legislature is even more skewed. Because of this,
    in most cases voting for the best Republicans in the primary is important.
    Closer to the primaries, we'll have a more detailed breakdown for people
    who are in districts that could swing either direction.
</p>
<p>As of this writing, the only clear primary vote is a vote for Liz Cheney
    for Wyoming's US House seat. Cheney is not going to work to reduce
    fossil fuels, but she has stood up for democracy in America, unlike the
    vast majority of her colleagues.</p>
<p>Wyoming <a href="https://oilcity.news/wyoming/legislature/2021/09/02/committee-hits-pause-on-proposal-to-create-open-primaries-in-wyoming/">does not have fully open primaries</a>,
    but does allow you to <a href="https://ballotpedia.org/Primary_elections_in_Wyoming">choose a party on election day</a>.
</p>
<p>We'll certainly want a more climate-friendly candidate in the general
    election, but we have to be realistic about our chances there.
</p>

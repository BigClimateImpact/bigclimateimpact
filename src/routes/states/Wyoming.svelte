<h2>Recommendations for Wyoming's primary elections</h2>
<p>Wyoming is a tough one for climate change, because fossil fuel industries
    are large and powerful in the state. Ideally, the state would be pushing to
    encourage new forms of energy investment, but here we are.
</p>
<p>Wyoming is a heavily Republican state, with about 70% of the presidential
    vote going to the Republican candidate in each of the last three elections.
    This makes it especially important that the Republican chosen for the House
    is the best we can get, even though they will not be in favor of addressing
    climate change.
</p>
<p>For that reason, voting for Liz Cheney in the Republican primary is the
    best bet. Cheney has stood up for democracy in America, and that may be
    the best we can get. Wyoming <a href="https://oilcity.news/wyoming/legislature/2021/09/02/committee-hits-pause-on-proposal-to-create-open-primaries-in-wyoming/">does not have fully open primaries</a>,
    but does allow you to <a href="https://ballotpedia.org/Primary_elections_in_Wyoming">choose a party on election day</a>.
</p>
<p><b>Important note:</b> This analysis does not yet include state-level
candidates. There may be enough potential among state-level candidates that it's
worth taking a different approach in the primaries. Stay tuned.
</p>
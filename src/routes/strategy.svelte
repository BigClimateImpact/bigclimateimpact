    <h2>Addressing climate change requires big levers</h2>
    <p>In the 2000s, fossil fuel giant <a href="https://mashable.com/feature/carbon-footprint-pr-campaign-sham">BP ran
        an ad campaign</a> to get people thinking about their individual
        "carbon footprint". While individual people do contribute to climate
        change in their daily lives, the fact of the matter is that there's
        a giant web of emissions that an individual is almost powerless to
        correct. In the Kurzgesagt video (which is well worth the 15 minutes),
        they mention that an American who manages to have zero emissions for
        their entire life would only offset a <em>single second</em> of global
        greenhouse gas emissions.
    </p>
    <p>But you <em>do</em> have one really large lever you can pull to
        deal with climate change: Your vote! Our political leaders at the
        federal, state, and local levels can create policies around
        electricity generation, help make it cheaper to buy electric cars,
        help fund development of technologies to help remove carbon from the
        air, and much more. They have the tools to open up economic opportunity
        <em>and</em> deal with climate change.
    </p>
    <p>Adding solar panels to your house is a step toward clean energy, but
        any gains would be <em>crushed</em> if you vote for a candidate who
        <a href="choosing">votes against policies to address climate change</a>.
    </p>
    <h2>Support candidates, vote in primaries</h2>
    <p>After you've taken the step of showing your support for
        the <a href="/bbb21">Build Back Better Act</a>, there's a lot
        you can do to support candidates who will take the next steps
        against climate change. <a href="/">Find your state</a> and read
        about the candidates who seek to do the work to fight climate
        change.
    </p>
    <h2>Big companies also have power over how we get our power</h2>
    <p>While the government has the greatest ability to change how
        Americans get their electricity, large companies use <em>a lot</em>
        of power and can push utilities to shift to renewables.
        One estimate puts the corporate sector at <a href="https://ihsmarkit.com/research-analysis/corporate-us-renewable-procurement-outlook-optimism.html">20% of utility-scale renewable additions</a>
        over the next decade.
    </p>
    <p>So, if you happen to work for a large company, raising awareness within
        your organization and encouraging movement toward reducing emissions at
        the corporate level is another lever you have access to that can have
        far more impact than your individual purchase choices.
    </p>

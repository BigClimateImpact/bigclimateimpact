    <h1>It's about 21<sup>st</sup> century prosperity</h1>

    <p>Something amazing happened in the 2010s. <a href="https://carbontracker.org/gain-not-pain-why-cop-must-move-the-narrative-forward/">The price of
        solar cells dropped 89%</a>, wind turbines fell in price by 59%,
        and lithium ion batteries became 89% more affordable. Renewables
        have become the cheapest form of electricity available!
    </p>
    <p>We can't afford to sit on the sidelines during this change.
        The move away from carbon-based energy is going to be an absolutely
        huge opportunity.
    </p>
    <p>A lot of this site is devoted to American citizens pushing the
        government to take action. What we're looking for is not, as some
        would have you believe, for the government to take over everything.
        Consider the Internet: <a href="https://en.wikipedia.org/wiki/Internet">It started
        with US government research and development</a>. The US government
        has enormous power to fund research and incentivize companies to
        take the long view rather than looking at next quarter's profits.
    </p>

    <p>The US <a href="https://www.brookings.edu/research/reforming-global-fossil-fuel-subsidies-how-the-united-states-can-restart-international-cooperation/">gives billions of dollars in
        subsidies to the fossil fuel industry</a> every year, despite the fact
        that burning fossil fuels are a <a href="https://www.reuters.com/business/cop/un-climate-negotiators-go-into-overtime-save-15-celsius-goal-2021-11-13/">"key driver of global warming</a>.
        Fossil fuels also kill millions of people every year due to pollution,
        lives that would be saved by using clean electricty wherever possible.
    </p>

    <h2>And it's about avoiding the peril</h2>

    <p>People who say that addressing climate change will be expensive are
        not only missing the incredible opportunity that exists, but are also
        overlooking the astronomical costs of not addressing climate change.
        The <a href="https://www.oecd.org/fr/environnement/climate-change-consequences-of-inaction.htm">OECD highlighted some of the consequences</a> (in 2015):
    </p>
    <ul>
        <li>$35 trillion in coastal city assets in danger from rising sea levels</li>
        <li>More droughts and more flooding, with trillions more in damages</li>
        <li>Millions of people would die prematurely</li>
    </ul>
    <p>As they say in that article, "Doing nothing will cost more than acting".</p>

    <h2>Act now for even better results</h2>

    <p>The longer we take to act, the more it will cost us. The longer
        America takes to embrace the economic changes, the more reliant we will
        be on other countries for the energy technologies of the 21<sup>st</sup>
        century.
    </p>
